//! # sago-proto
//!
//! Placeholder crate for Sago's gRPC / Protocol Buffer definitions, intended to
//! back a future microservice / plugin interface.
//!
//! This crate currently contains no service definitions. The `.proto` files and
//! their generated bindings are tracked under Phase 4 of `docs/ROADMAP.md` and
//! are blocked on a `protoc` toolchain being available in the build environment.
//!
//! It is marked `publish = false` and exists so the workspace layout and
//! documentation stay aligned with the planned architecture.
